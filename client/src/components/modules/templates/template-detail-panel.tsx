function StructureNode({
  node,
  depth,
  path,
  selectedPath,
  onSelect,
}: {
  node: TemplateNode;
  depth: number;
  path: number[];
  selectedPath: number[] | null;
  onSelect: (path: number[]) => void;
}) {
  const hasChildren = node.children && node.children.length > 0;
  const isSelected = selectedPath !== null && path.join('-') === selectedPath.join('-');

  return (
    <div>
      <div
        className="flex items-center"
        style={{
          height: 26,
          paddingLeft: depth * 16 + 8,
          gap: 6,
          fontSize: 'var(--text-xs)',
          borderRadius: 'var(--radius-sm)',
          backgroundColor: isSelected ? 'var(--accent-subtle)' : 'transparent',
          cursor: 'pointer',
        }}
        onClick={() => onSelect(path)}
        onMouseEnter={(e) => {
          if (!isSelected) e.currentTarget.style.backgroundColor = 'var(--surface-hover)';
        }}
        onMouseLeave={(e) => {
          if (!isSelected) e.currentTarget.style.backgroundColor = 'transparent';
        }}
      >
        <span style={{ color: 'var(--text-tertiary)', fontWeight: 500, minWidth: 32, flexShrink: 0 }}>
          {node.tag}
        </span>
        <span
          style={{
            fontFamily: 'var(--font-mono)',
            color: 'var(--accent-text)',
            backgroundColor: isSelected ? 'var(--accent)' : 'var(--accent-subtle)',
            ...(isSelected ? { color: 'var(--text-on-accent)' } : {}),
            padding: '0 4px',
            borderRadius: 'var(--radius-sm)',
            whiteSpace: 'nowrap',
            overflow: 'hidden',
            textOverflow: 'ellipsis',
          }}
        >
          .{node.className}
        </span>
        {node.text && (
          <span
            style={{
              color: 'var(--text-tertiary)',
              whiteSpace: 'nowrap',
              overflow: 'hidden',
              textOverflow: 'ellipsis',
              fontStyle: 'italic',
            }}
          >
            "{node.text}"
          </span>
        )}
      </div>
      {hasChildren &&
        node.children!.map((child, i) => (
          <StructureNode
            key={`${child.className}-${i}`}
            node={child}
            depth={depth + 1}
            path={[...path, i]}
            selectedPath={selectedPath}
            onSelect={onSelect}
          />
        ))}
    </div>
  );
}

function CollapsibleSection({ title, defaultOpen = false, children }: { title: string; defaultOpen?: boolean; children: React.ReactNode }) {
  const [open, setOpen] = useState(defaultOpen);

  return (
    <div
      style={{
        border: '1px solid var(--border-default)',
        borderRadius: 'var(--radius-lg)',
        overflow: 'hidden',
      }}
    >
      <button
        onClick={() => setOpen((prev) => !prev)}
        className="flex items-center justify-between"
        style={{
          width: '100%',
          height: 36,
          padding: '0 12px',
          border: 'none',
          borderBottom: open ? '1px solid var(--border-default)' : '1px solid transparent',
          backgroundColor: 'var(--bg-secondary)',
          cursor: 'pointer',
          fontFamily: 'var(--font-sans)',
        }}
      >
        <span
          className="font-medium"
          style={{ fontSize: 'var(--text-xs)', color: 'var(--text-primary)' }}
        >
          {title}
        </span>
        <ChevronDown
          size={14}
          style={{
            color: 'var(--text-tertiary)',
            transform: open ? 'rotate(0deg)' : 'rotate(-90deg)',
            transition: 'transform var(--duration-fast)',
          }}
        />
      </button>
      {open && <div style={{ padding: 12 }}>{children}</div>}
    </div>
  );
}

function countNodes(node: TemplateNode): number {
  return 1 + (node.children?.reduce((sum, child) => sum + countNodes(child), 0) ?? 0);
}
